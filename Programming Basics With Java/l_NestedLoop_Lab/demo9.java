package l_NestedLoop_Lab;

public class demo9 {
    public static void main(String[] args) {
        int a = 3;
        int b = 3;
        for (int i = 0; i < a; i++) {
            System.out.println("i= " + i);
            for (int j = 0; j < b; j++) {
                System.out.println("j= " + j);
            }
            System.out.println();

        }

    }
}
