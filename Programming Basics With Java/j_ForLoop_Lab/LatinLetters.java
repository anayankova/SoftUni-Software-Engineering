package j_ForLoop_Lab;

public class LatinLetters {
    public static void main(String[] args) {
        for (char i = 'a'; i <= 'z'; i++){
            System.out.println(i);
        }

        System.out.print("Latin alphabet:");

        for (char letter = 'a'; letter <= 'z'; letter++) {
            System.out.print(" " + letter);
        }


    }
}
