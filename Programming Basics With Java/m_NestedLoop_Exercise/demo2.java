package m_NestedLoop_Exercise;

public class demo2 {
    public static void main(String[] args) {

        for (char i = 'a'; i < 'c'; i++) {
            System.out.println(i);

        }

        for (int i = 'a'; i < 'c'; i++) {
            System.out.println(i);

        }

        for (int i = 'a'; i < 'c'; i++) {
            System.out.printf("%c%n",i);

        }

    }
}
