public class demo11Boolean {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        boolean greaterAB = (a > b);
        System.out.println(greaterAB);  // False
        boolean equalA1 = (a == 1);
        System.out.println(equalA1);    // True


    }
}
