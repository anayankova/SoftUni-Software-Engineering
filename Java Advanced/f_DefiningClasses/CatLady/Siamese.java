package f_DefiningClasses.CatLady;

public class Siamese {
    private String name;
    private double earSize;

    public Siamese(String name, double earSize) {
        this.name = name;
        this.earSize = earSize;
    }

    public String getName() {
        return this.name;
    }

    public double getEarSize() {
        return this.earSize;
    }
}
